/*                formas de declaração                 */

/* 
exite em js varias formas de declarar uma var, sendo umas delas a usando forma de "letras/palavras" EX: "var x = 1", 
A utilização de de "$" antes de declarar o valor da var EX 'var $n = 1',
e a utilização de "_" antes de declarar o valor da var EX 'var _x = 1'

*/

/*                Regras a se seguir                 */


/*

-> Não começar a declaração da variavel com numeros EX; var 1n = 10; (está errado)

-> É possível usar letras ou númeroa Ex; var n1 = 10; (está certo)

-> É possível a utilização de acentos esímbolos EX; var média = 6; (está certo)

-> Não poe conter espaçamentos nas declarações EX; var média ponderada = 6; (está errado) ou invés disso utilize EX; var média_ponderada = 10; (está certo)

-> Não utiizar palavras resevadas ou seja palavras pré-definidas da linguagens EX; var var = 10; (está errado), utlizar apenas palvaras que não está reservadas, como EX; var valor = 10;


*/


